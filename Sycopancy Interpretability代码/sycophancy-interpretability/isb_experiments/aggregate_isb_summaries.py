#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Aggregate ISB summary JSON files into a concise markdown comparison table.
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import Dict, List


def load_json(path: Path) -> Dict:
    return json.loads(path.read_text(encoding="utf-8"))


def ci_cross_zero(ci: List[float]) -> bool:
    lo, hi = float(ci[0]), float(ci[1])
    return lo <= 0.0 <= hi


def fmt(x: float, nd: int = 4) -> str:
    return f"{x:.{nd}f}"


def fmt_ci(ci: List[float]) -> str:
    return f"[{ci[0]:.4f}, {ci[1]:.4f}]"


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument(
        "--inputs",
        nargs="+",
        required=True,
        help="Summary JSON files generated by analyze_isb_metrics.py --summary_json",
    )
    ap.add_argument(
        "--output_md",
        default="isb_experiments/isb_summary_comparison.md",
        help="Output markdown path.",
    )
    args = ap.parse_args()

    rows = []
    for s in args.inputs:
        p = Path(s).resolve()
        d = load_json(p)
        ci = d.get("ATE_bootstrap_CI95", {})
        row = {
            "run": p.stem,
            "rows": d.get("rows_parsed", 0),
            "wrong_follow": d.get("overall_wrong_follow_rate", 0.0),
            "correct": d.get("overall_correct_rate", 0.0),
            "ate_opinion": d.get("ATE_global", {}).get("opinion", 0.0),
            "ate_person_g": d.get("ATE_global", {}).get("person", 0.0),
            "ate_person_s": d.get("ATE_support_only", {}).get("person", 0.0),
            "ate_challenge_g": d.get("ATE_global", {}).get("challenge", 0.0),
            "mss": d.get("MSS", 0.0),
            "ci_person_global": ci.get("person_global", [0.0, 0.0]),
            "ci_authority_global": ci.get("authority_global", [0.0, 0.0]),
            "ci_challenge_global": ci.get("challenge_global", [0.0, 0.0]),
            "ci_person_support": ci.get("person_support", [0.0, 0.0]),
            "path": str(p),
        }
        rows.append(row)

    lines = []
    lines.append("# ISB Summary Comparison")
    lines.append("")
    lines.append("| run | n | wrong_follow | correct | ATE_opinion | ATE_person_g | ATE_person_s | ATE_challenge_g | MSS |")
    lines.append("|---|---:|---:|---:|---:|---:|---:|---:|---:|")
    for r in rows:
        lines.append(
            "| {run} | {rows} | {wrong_follow} | {correct} | {ate_opinion} | {ate_person_g} | {ate_person_s} | {ate_challenge_g} | {mss} |".format(
                run=r["run"],
                rows=r["rows"],
                wrong_follow=fmt(r["wrong_follow"]),
                correct=fmt(r["correct"]),
                ate_opinion=fmt(r["ate_opinion"]),
                ate_person_g=fmt(r["ate_person_g"]),
                ate_person_s=fmt(r["ate_person_s"]),
                ate_challenge_g=fmt(r["ate_challenge_g"]),
                mss=fmt(r["mss"]),
            )
        )

    lines.append("")
    lines.append("## CI Diagnostic")
    lines.append("")
    lines.append("| run | person_global CI95 | cross 0? | authority_global CI95 | cross 0? | challenge_global CI95 | cross 0? | person_support CI95 | cross 0? |")
    lines.append("|---|---:|---:|---:|---:|---:|---:|---:|---:|")
    for r in rows:
        pgi = r["ci_person_global"]
        agi = r["ci_authority_global"]
        cgi = r["ci_challenge_global"]
        psi = r["ci_person_support"]
        lines.append(
            "| {run} | {pgi} | {pgi_x} | {agi} | {agi_x} | {cgi} | {cgi_x} | {psi} | {psi_x} |".format(
                run=r["run"],
                pgi=fmt_ci(pgi),
                pgi_x=str(ci_cross_zero(pgi)),
                agi=fmt_ci(agi),
                agi_x=str(ci_cross_zero(agi)),
                cgi=fmt_ci(cgi),
                cgi_x=str(ci_cross_zero(cgi)),
                psi=fmt_ci(psi),
                psi_x=str(ci_cross_zero(psi)),
            )
        )

    lines.append("")
    lines.append("## Input Files")
    lines.append("")
    for r in rows:
        lines.append(f"- `{r['run']}`: `{r['path']}`")

    out = Path(args.output_md).resolve()
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text("\n".join(lines), encoding="utf-8")
    print(f"[saved] {out}")


if __name__ == "__main__":
    main()

